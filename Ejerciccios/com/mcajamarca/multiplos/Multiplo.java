package com.mcajamarca.multiplos;

public class Multiplo {
	
	public static boolean esPar(int número){
		return (número%2==0)?true:false;
	}
	
	public static boolean esImpar(int número){
		return (número%2==1)?true:false;
	}
	
	public static boolean esImpar(int númeroUno, int númeroDos){
		return (númeroUno%númeroDos==0)?true:false;
	}
	

}
